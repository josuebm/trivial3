    public void rellenarArrayList(){
        lado = new ArrayList();
        for(int i=0; i<72; i++){
            String k = String.format("%02d", i);
            lado.add(new ImageIcon(getClass().getResource("../imagenes/dado/dado"+ k + ".png")));
        }
    }
    
    
