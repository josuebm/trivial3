    public void girarDado(){
        //Manejamos si es la primera o la segunda pulsacion en el dado, para ponerlo a girar o pararlo
          if(primera){
            hilo = new Thread(this);
            hilo.start();
            primera = false;
        }else{
            hilo.stop();
            primera = true;
        }
          
          
        String imagen = dadoL.getIcon().toString();
        imagen = imagen.substring(imagen.length()-6, imagen.length()-4);
        int im = Integer.valueOf(imagen);
        if(im >= 00 && im <= 05 || im >= 66 && im <= 71)
            dadoL.setIcon(lado.get(0));
        else
            if(im >= 06 && im <= 11 || im >= 42 && im <= 47)
                dadoL.setIcon(lado.get(45));
            else
                if(im >= 12 && im <= 23)
                    dadoL.setIcon(lado.get(18));
                else
                    if(im >= 24 && im <= 29 || im >= 60 && im <= 65)
                        dadoL.setIcon(lado.get(63));
                    else
                        if(im >= 30 && im <= 41)
                            dadoL.setIcon(lado.get(36));
                        else
                            if(im >= 48 && im <= 59)
                                dadoL.setIcon(lado.get(54));
        imagen = dadoL.getIcon().toString();
        imagen = imagen.substring(imagen.length()-6, imagen.length()-4);
        im = Integer.valueOf(imagen);
        switch(im){
            case 0:{
                tir = 6;
            }break;
            case 45:{
                tir = 2;
            }break;
            case 18:{
                tir = 3;
            }break;
            case 63:{
                tir = 5;
            }break;
            case 36:{
                tir = 1;
            }break;
            case 54:{
                tir = 4;
            }
        }
    }
